    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            // TODO add your handling code here:
            if (taAlamat.getText().isEmpty() || tfStatus.getText().isEmpty()|| tfJarak.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Format masih kosong");
            } else {
                paket.setAlamatTujuan(taAlamat.getText());
                paket.setStatus(tfStatus.getText());
                paket.setJarak(Integer.parseInt(tfJarak.getText()));
                DATA.updateDATA(paket);
                JOptionPane.showMessageDialog(this, "Edit paket berhasil");
                dispose();
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Tidak bisa menambah paket");
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Format jarak salah");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

