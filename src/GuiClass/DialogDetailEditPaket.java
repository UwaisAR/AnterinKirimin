    private void loadData() throws SQLException{
        DATA.loadDATA();
        paket = DATA.listPaket.stream().filter(t -> t.getNoResi().equals(GUIMenuAdmin.jTable1.getValueAt(GUIMenuAdmin.jTable1.getSelectedRow(), 0))).findFirst().orElse(null);
        lUsernameSender.setText(paket.getPengirim().getUsername());
        lTanggalKirim.setText(paket.getTanggalDibuat().format(DateTimeFormatter.ofLocalizedDate(FormatStyle.FULL)));
        if (paket.getTanggalSampai()!=null)lTanggalSampai.setText(paket.getTanggalSampai().format(DateTimeFormatter.ofLocalizedDate(FormatStyle.FULL)));
        else lTanggalSampai.setText(paket.getEstimasiSampai());
        tfStatus.setText(paket.getStatus());
        lTipePaket.setText(paket.getTipe());
        taAlamat.setText(paket.getAlamatTujuan());
        tfJarak.setText(paket.getJarak()+"");
        lHargaPaket.setText("Rp. "+paket.hargaBarang()+" (+"+(paket.hargaPajak())+")");
        lNomorResi1.setText(paket.getNoResi());
    }
    
    private void showTable() throws SQLException{
        try {
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("Nama");
            model.addColumn("Berat");
            model.addColumn("Dimensi");
            jTable1.setModel(model);
            for (Item item : paket.getItem()) {
                model.addRow(new Object[]{item.getNama(), item.getBerat()+"kg", item.getDimensi()[0]+"cm x"+item.getDimensi()[1]+"cm x"+item.getDimensi()[2]+"cm"});
            }
            jTable1.setModel(model);
        } catch (Exception e) {
            throw e;
        }
    }
