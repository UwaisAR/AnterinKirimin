    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            if(tfUsername.getForeground()==Color.BLACK&&
               tfPassword.getForeground()==Color.BLACK&&
               tfNoTelepon.getForeground()==Color.BLACK&&
               taAlamat.getForeground()==Color.BLACK)
            {   
                assert (tfNoTelepon.getText()).matches("\\d{10,13}") : JOptionPane.showOptionDialog(null, "Nomor Telepon tidak valid", "Message", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE, null, null, null);
                assert (tfPassword.getText()).length()>=8 : JOptionPane.showOptionDialog(null, "Password kurang dari 8", "Message", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE, null, null, null);
                assert DATA.listGudang.stream().anyMatch(t -> taAlamat.getText().toLowerCase().contains(t.getKota().toLowerCase())) : JOptionPane.showOptionDialog(null, "Layanan belum tersedia di kota anda", "Message", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE, null, null, null);
                if (!jLabel1.getText().toLowerCase().contains("edit")){
                    assert !DATA.listAkun.stream().anyMatch(t -> t instanceof Pengirim && t.getUsername().equalsIgnoreCase(tfUsername.getText())) : JOptionPane.showOptionDialog(null, "Username sudah ada", "Message", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE, null, null, null);
                    DATA.addDATA(pengirim);
                }
                pengirim.setUsername(tfUsername.getText());
                pengirim.setPassword(tfPassword.getText());
                pengirim.setNoHp(tfNoTelepon.getText());
                pengirim.setAlamat(taAlamat.getText());
                DATA.updateDATA(pengirim);
                switch (jLabel1.getText()) {
                    case "Edit User":
                        JOptionPane.showMessageDialog(this, "Berhasil diperbarui");
                        GUIMenuAdmin.showTable();
                        break;
                    case "Register User":
                        JOptionPane.showMessageDialog(this, "Registrasi berhasil");
                        break;
                    case "Edit Profil":
                        JOptionPane.showMessageDialog(this, "Berhasil diperbarui");
                        GUILogin.tfUsername.setText(pengirim.getUsername());
                        GUILogin.tfPassword1.setText(pengirim.getPassword());
                        GUIMenuPengirim.jLabel1.setText("Selamat datang, "+pengirim.getUsername());
                        GUIMenuPengirim.loadData();
                        break;
                }
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Format salah atau belum diisi");
            }
        } catch (HeadlessException | SQLException e){
            JOptionPane.showMessageDialog(this, "Register gagal");
        } catch (AssertionError e){}
    }//GEN-LAST:event_jButton1ActionPerformed

