    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        try {
            if(pengirim.getPaket().get(tableCount).getStatus().equals("WAITING FOR PAYMENT")||pengirim.getPaket().get(tableCount).getStatus().contains("WRONG")) new DialogEditTambahPaketItem(this, true).setVisible(true);
            else JOptionPane.showMessageDialog(this, "Paket yang dipilih sudah dibayar");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Paket belum dipilih");
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        try {
            // TODO add your handling code here:
            if (tableCount<0) throw new Exception("Paket belum dipilih");
            if (pengirim.getPaket().get(tableCount).getStatus().equals("WAITING FOR PAYMENT")||pengirim.getPaket().get(tableCount).getStatus().contains("RECEIVED")){
                DATA.hapusDATA(pengirim.getPaket().get(tableCount));
                showTable();
                JOptionPane.showMessageDialog(this, "Berhasil dihapus");
            } else {
                throw new Exception("Paket yang dipilih tidak bisa dihapus");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Terjadi Kesalahan\n"+e.getMessage());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnHapusActionPerformed

