    private void detailData() {
        lNomorResi.setText(pengirim.getPaket().get(tableCount).getNoResi());
        lTanggalKirim.setText(pengirim.getPaket().get(tableCount).getTanggalDibuat().format(DateTimeFormatter.ofLocalizedDate(FormatStyle.FULL)));
        if (pengirim.getPaket().get(tableCount).getTanggalSampai()!=null)lTanggalSampai.setText(pengirim.getPaket().get(tableCount).getTanggalSampai().format(DateTimeFormatter.ofLocalizedDate(FormatStyle.FULL)));
        else lTanggalSampai.setText(pengirim.getPaket().get(tableCount).getEstimasiSampai());
        lStatusPaket.setText(pengirim.getPaket().get(tableCount).getStatus());
        lTipePaket.setText(pengirim.getPaket().get(tableCount).getTipe());
        taAlamat.setText(pengirim.getPaket().get(tableCount).getAlamatTujuan());
        lJarak.setText(pengirim.getPaket().get(tableCount).getJarak()+" Km");
        lHargaPaket.setText("Rp. "+pengirim.getPaket().get(tableCount).hargaBarang()+" (+"+(pengirim.getPaket().get(tableCount).hargaPajak())+")");
    }
    
    public static void loadData() throws SQLException{
        pengirim = (Pengirim) DATA.authAccount(GUILogin.tfUsername.getText(), GUILogin.tfPassword1.getText());
    }
    
    public static void showTable() throws SQLException{
        DATA.loadDATA();
        loadData();
        try {
            DefaultTableModel model = new DefaultTableModel();
            for (String string : kolomPaket) {
                model.addColumn(string);
            }
            for (Paket pkg : pengirim.getPaket()) {
                model.addRow(new Object[]{pkg.getNoResi(), pkg.getTanggalSampai()!=null?pkg.getTanggalSampai():pkg.getEstimasiSampai(), pkg.getTipe(), pkg.getStatus(), pkg.hargaBarang()+pkg.hargaPajak()});
            }
            jTable1.setModel(model);
        } catch (Exception ex) {
            Logger.getLogger(GUIMenuPengirim.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
