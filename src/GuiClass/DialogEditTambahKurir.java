    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(tfUsername.getForeground()==Color.BLACK&&
            tfPassword.getForeground()==Color.BLACK&&
            tfNoTelepon.getForeground()==Color.BLACK&&
            tfKota.getForeground()==Color.BLACK)
        {
            try {
                kurir.setUsername(tfUsername.getText());
                kurir.setPassword(tfPassword.getText());
                kurir.setNoHp(tfNoTelepon.getText());
                kurir.setKota(tfKota.getText());
                DATA.updateDATA(kurir);
                assert (tfNoTelepon.getText()).matches("\\d{10,13}") : JOptionPane.showOptionDialog(null, "Nomor Telepon tidak valid", "Message", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE, null, null, null);
                assert (tfPassword.getText()).length()>=8 : JOptionPane.showOptionDialog(null, "Password kurang dari 8", "Message", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE, null, null, null);
                assert DATA.listGudang.stream().anyMatch(t -> tfKota.getText().toLowerCase().contains(t.getKota().toLowerCase())) : JOptionPane.showOptionDialog(null, "Layanan belum tersedia di kota anda", "Message", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE, null, null, null);
                if (!jLabel1.getText().toLowerCase().contains("edit")){
                    assert !DATA.listAkun.stream().anyMatch(t -> t instanceof Kurir && t.getUsername().equalsIgnoreCase(tfUsername.getText())) : JOptionPane.showOptionDialog(null, "Username sudah ada", "Message", JOptionPane.INFORMATION_MESSAGE, JOptionPane.INFORMATION_MESSAGE, null, null, null);
                    DATA.addDATA(kurir);
                }
                switch (jLabel1.getText()) {
                    case "Edit Kurir":
                        JOptionPane.showMessageDialog(this, "Berhasil diperbarui");
                        GUIMenuAdmin.showTable();
                        break;
                    case "Tambah Kurir":
                        JOptionPane.showMessageDialog(this, "Berhasil ditambah");
                        GUIMenuAdmin.showTable();
                        break;
                    case "Edit Profil":
                        JOptionPane.showMessageDialog(this, "Berhasil diperbarui");
                        GUILogin.tfUsername.setText(kurir.getUsername());
                        GUILogin.tfPassword1.setText(kurir.getPassword());
                        GUIMenuKurir.jLabel1.setText("Selamat datang, "+kurir.getUsername());
                        GUIMenuKurir.loadData();
                        break;
                }
                dispose();
            } catch (NullPointerException | HeadlessException | SQLException ex) {
                JOptionPane.showMessageDialog(this, "Terjadi Kesalahan");
            } catch (AssertionError e){}
        } else {
            JOptionPane.showMessageDialog(this, "Format salah atau belum diisi");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

