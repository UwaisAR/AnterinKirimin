            tfUsername.setText(GUIMenuAdmin.getTableValue(1));
            tfPassword.setText(GUIMenuAdmin.getTableValue(2));
            tfNoTelepon.setText(GUIMenuAdmin.getTableValue(3));
            tfKota.setText(GUIMenuAdmin.getTableValue(4));
