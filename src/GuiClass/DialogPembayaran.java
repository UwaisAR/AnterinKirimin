    private void tfNoRekeningKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfNoRekeningKeyReleased
        // TODO add your handling code here:
        if(listPembayaran.stream().anyMatch((t) -> t.getNoKartu().equals(tfNoRekening.getText()))){
            pembayaran = listPembayaran.stream().filter((t) -> t.getNoKartu().equals(tfNoRekening.getText())).findFirst().orElse(null);
            lSaldo.setText(lSaldo.getText().replace("0,-", pembayaran.getSaldo()+""));
        } else {
            lSaldo.setText("Rp. 0,-");
        }
    }//GEN-LAST:event_tfNoRekeningKeyReleased

