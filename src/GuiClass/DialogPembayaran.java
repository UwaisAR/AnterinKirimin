    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            if(listPembayaran.stream().anyMatch((t) -> t.getNoKartu().equals(tfNoRekening.getText())&&t.getPin().equals(tfPin.getText()))){
                if (pembayaran.getSaldo()-(GUIMenuPengirim.pengirim.getPaket().get(GUIMenuPengirim.tableCount).hargaBarang()+GUIMenuPengirim.pengirim.getPaket().get(GUIMenuPengirim.tableCount).hargaPajak())<0){
                    throw new Exception("Uang tidak cukup");
                }
                getPembayaranDAO().updateBalance(pembayaran.getNoKartu(), pembayaran.getSaldo()-(GUIMenuPengirim.pengirim.getPaket().get(GUIMenuPengirim.tableCount).hargaBarang()+GUIMenuPengirim.pengirim.getPaket().get(GUIMenuPengirim.tableCount).hargaPajak()));
                DATA.loadDATA();
                JOptionPane.showMessageDialog(this, "Berhasil Dibayar");
                GUIMenuPengirim.pengirim.getPaket().get(GUIMenuPengirim.tableCount).setStatus("WAITING");
                DATA.updateDATA(GUIMenuPengirim.pengirim.getPaket().get(GUIMenuPengirim.tableCount));
                GUIMenuPengirim.showTable();
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Format nomor rekening atau pin salah/kosong");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Kesalahan transaksi");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

